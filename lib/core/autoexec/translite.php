	function cmsTranslite($text) {
		
		$array = array(
			
			"" => "a",		"" => "A",
			"" => "b",		"" => "B",
			"" => "v",		"" => "V",
			"" => "g",		"" => "G",
			"" => "d",		"" => "D",
			"" => "e",		"" => "E",
			"" => "e",		"" => "E",
			"" => "j",		"" => "J",
			"" => "z",		"" => "Z",
			"" => "i",		"" => "I",
			"" => "i'",	"" => "I'",
			"" => "k",		"" => "K",
			"" => "l",		"" => "L",
			"" => "m",		"" => "M",
			"" => "n",		"" => "N",
			"" => "o",		"" => "O",
			"" => "p",		"" => "P",
			"" => "r",		"" => "R",
			"" => "s",		"" => "S",
			"" => "t",		"" => "T",
			"" => "u",		"" => "U",
			"" => "f",		"" => "F",
			"" => "h",		"" => "H",
			"" => "c",		"" => "C",
			"" => "ch",	"" => "Ch",
			"" => "sh",	"" => "Sh",
			"" => "sh'",	"" => "Sh'",
			"" => "'",		"" => "'",
			"" => "i",		"" => "I",
			"" => "'",		"" => "'",
			"" => "e",		"" => "E",
			"" => "y",		"" => "Y",
			"" => "ya",	"" => "Ya"
			
		);
		
		foreach ($array as $k=>$v) {
			
			$pregs[]    = '/' . $k . '/';
			$replaces[] = $v;
			
		}
		
		return preg_replace($pregs, $replaces, $text);
		
	}
	
